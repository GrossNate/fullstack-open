import { Gender } from '../types';
import z from 'zod';

export const patientZodSchema = z.object({
  name: z.string(),
  dateOfBirth: z.string(),
  ssn: z.string(),
  gender: z.nativeEnum(Gender),
  occupation: z.string(),
  id: z.string()
});

